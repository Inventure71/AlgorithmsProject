from core.linked_list import Node

class Queue:
    def __init__(self):
        self.head = None
        self.tail = None

    def enqueue(self, value):
        new_node = Node(value)
        if self.head == None:
            self.head = new_node
            self.tail = new_node
        else:
            self.tail.next = new_node
            self.tail = new_node
    
    def dequeue(self):
        if self.head != None:
            value = self.head.value
            self.head = self.head.next
            if self.head == None:
                self.tail = None
            return value
        return None


